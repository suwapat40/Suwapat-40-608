<?php

// กำหนดตัวแปรสมมติเงินของนายสมชาย (สามารถเปลี่ยนตัวเลขตรงนี้เพื่อทดสอบรันโค้ดได้)
$money = 350; 

// ตรวจสอบเงื่อนไขตามจำนวนเงิน
if ($money < 500) {
    echo "นายสมชาย ใจดี มีเงิน " . $money . " บาท : เขาจะสามารถซื้อกางเกงได้";
} 
elseif ($money < 1000) {
    echo "นายสมชาย ใจดี มีเงิน " . $money . " บาท : เขาจะสามารถซื้อกางเกงและเสื้อได้";
} 
elseif ($money < 1500) {
    echo "นายสมชาย ใจดี มีเงิน " . $money . " บาท : สามารถซื้อกางเกง เสื้อ และรองเท้าได้";
} 
else {
    echo "นายสมชาย ใจดี มีเงิน " . $money . " บาท : เขามีเงินนอกเหนือจากเงื่อนไขขั้นต้น (1,500 บาทขึ้นไป)";
}

?>